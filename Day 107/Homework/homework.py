def to_left(list):
    lst = []
    counter = 0
    for i in range(len(list)):
        counter = 0
        for j in range(i):
            if list[i] > list[j]:
                counter += 1
        lst.append(counter)
    return lst
print(to_left([3, 2, 6, 5, 1]))
            
